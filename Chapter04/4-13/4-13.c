#include <stdio.h>

int main(void) 
{
	int a = 10;									// 비트열 00000000 00000000 00000000 00001010
	int b = 12;									// 비트열 00000000 00000000 00000000 00001100

	printf("a & b : %d\n", a & b);		// 8	// 비트열 00000000 00000000 00000000 00001000
	printf("a ^ b : %d\n", a ^ b);		// 6	// 비트열 00000000 00000000 00000000 00000110
	printf("a | b : %d\n", a | b);		// 14	// 비트열 00000000 00000000 00000000 00001110
	printf("~a : %d\n", ~a);			// -11	// 비트열 11111111 11111111 11111111 11110101
	printf("a << 1 : %d\n", a << 1);	// 20	// 비트열 00000000 00000000 00000000 00010100
	printf("a >> 2 : %d\n", a >> 2);	// 2	// 비트열 00000000 00000000 00000000 00000010)

	return 0;
}